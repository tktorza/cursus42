#include "../inc/libft.h"

void	ft_strclr(char *s)
{
	if (s == NULL)
		return ;
	while (*s)
	{
		while (*s == '\0')
		{
			*s = '\0';
			s++;
		}
		*s = '\0';
		s++;
	}
}
